#include "libft.h"

char	*ft_strnew(size_t n)
{
	char	*t;
	size_t	i;

	i = 0;
	if (!(t = (char *)malloc(n + 1)) || n + 1 < n)
		return (NULL);
	while (i < n)
	{
		t[i++] = 0;
	}
	t[i] = '\0';
	return (t);
}
